        <h3>Onze Groene Missie: Duurzaam Genieten bij Bread Company</h3>
        <p>
            Bij Bread Company geloven we dat heerlijk eten hand in hand kan gaan met een gezonde planeet. Daarom 
            hebben we duurzaamheid niet alleen opgenomen in onze missie, maar verweven we het in alles wat we doen 
            — van inkoop tot verpakking en bezorging. Zo zorgen we ervoor dat elke hap niet alleen lekker is, maar 
            ook goed voelt.
        </p>

        <h3>Lokale Ingrediënten, Wereld van Verschil</h3>
        <p>
        Onze reis naar duurzaamheid begint bij de bron: de ingrediënten. Waar mogelijk kopen we onze grondstoffen 
        lokaal in. Onze bloem komt van Nederlandse molenaars die graan verwerken van boeren uit de regio. Onze 
        eieren, melk en boter worden geleverd door boerderijen binnen een straal van 50 kilometer van onze 
        bakkerij. Door lokaal in te kopen, beperken we het aantal transportkilometers drastisch. Minder 
        vrachtwagens op de weg betekent minder CO₂-uitstoot, én een eerlijkere prijs voor lokale producenten. 
        Zo bouwen we samen aan een sterkere lokale economie én een kleinere ecologische voetafdruk.
        </p>

        <h3>Seizoensgebonden en zonder verspilling</h3>
        <p>
            We passen ons assortiment aan op het seizoen. Dit betekent dat je bij Bread Company in de lente andere 
            smaken tegenkomt dan in de herfst. Niet alleen zorgt dit voor afwisseling en inspiratie, het voorkomt 
            ook onnodige opslag en verspilling van producten die niet langer vers zijn. Producten die aan het eind 
            van de dag niet verkocht zijn, worden niet weggegooid. We doneren deze aan lokale voedselbanken of 
            gebruiken ze voor het maken van nieuwe producten zoals croutons, paneermeel of broodpudding.
        </p>

        <h3>Milieuvriendelijke verpakkingen</h3>
        <p>
            Verpakking is een essentieel onderdeel van ons proces, vooral omdat we bezorgen aan huis. Toch willen we 
            hier zo min mogelijk afval mee veroorzaken. Daarom gebruiken we uitsluitend biologisch afbreekbare, 
            composteerbare of herbruikbare verpakkingsmaterialen. Onze broodzakken zijn gemaakt van gerecycled papier 
            met een plantaardige coating, en onze gebakdozen bevatten geen plastic vensters meer, maar wel een QR-code 
            die leidt naar foto&quot;s van het gebak. Voor koude producten maken we gebruik van isolatieverpakkingen op 
            basis van maïszetmeel of gerecycled karton, die volledig afbreekbaar zijn.
        </p>

        <h3>Energiezuinig bakken</h3>
        <p>
            Onze bakkerij maakt gebruik van moderne ovens en apparatuur die draaien op groene stroom. Onze elektriciteit 
            komt van zonnepanelen op het dak van onze bakkerij en een lokaal windmolenproject waar we actief in 
            participeren. Daarnaast worden onze ovens zodanig geprogrammeerd dat restwarmte efficiënt wordt 
            hergebruikt. Warmte die vrijkomt bij het bakken wordt bijvoorbeeld gebruikt voor het verwarmen van het 
            water in onze afwasinstallaties of het drogen van handdoeken.
        </p>

        <h3>Duurzame bezorging</h3>
        <p>
            Ook onze bezorging is afgestemd op milieuvriendelijkheid. In stedelijke gebieden leveren wij uitsluitend 
            met elektrische bakfietsen of bezorgwagens op stroom. In landelijke gebieden combineren wij bestellingen 
            slim om onnodige ritten te vermijden. Bovendien zijn onze bezorgers getraind in “groen rijden” en plannen 
            we onze routes zodanig dat brandstofverbruik tot een minimum beperkt blijft.
        </p>

        <h3>Bewustwording en transparantie</h3>
        <p>
            Tot slot geloven we in de kracht van bewustwording. We vertellen onze klanten graag over onze keuzes en 
            de impact daarvan. Op elke verpakking vind je een kort verhaal over de herkomst van het product of de 
            verpakking, en op onze website kun je onze duurzaamheidsinspanningen volgen in cijfers. Zo houden we 
            onszelf verantwoordelijk en maken we duurzaamheid concreet en meetbaar.
        </p>

        <p>
